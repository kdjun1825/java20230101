package ex2_switch;

public class Ex1_switch {

	public static void main(String[] args) {

		
		//스위치문은 비교값으로 딱 세가지 타입만 가능 (정수, 문자열, 문자)
		char ch = '이';
		
		switch ( ch ) {
		
		case '김' :
			System.out.println("21%");
			break;
		case '이' :
			System.out.println("14%");
			break;
		case '박' :
			System.out.println("8%");
			break;
		
		}
		
		
	}

}
